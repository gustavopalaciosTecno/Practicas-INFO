"""
14-Escribe un programa que solicite al usuario un número entero y luego
imprima el doble y el triple de ese número.
"""

numero_entero = int(input("Introducí un número entero: "))
doble = numero_entero * 2
triple = numero_entero * 3
print(f"el doble de {numero_entero} es: {doble} y el triple es: {triple}")
