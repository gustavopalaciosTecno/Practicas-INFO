"""
7-Escribe un programa que calcule el área de un triángulo a partir de la base y la
altura dadas.
"""
print("##### cálculo del área de un triángulo#####")
base = int(input("coloca un valor numérico para la base: "))
altura = int(input("coloca un valor numérico para la altura: "))

resultado = (base * altura/2)
print(resultado)
